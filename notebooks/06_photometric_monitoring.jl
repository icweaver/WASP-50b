# ‚ïî‚ïê‚ï° 28ce86fd-9679-4775-80ff-75479baa8a0c
# Given a sorted vector `v`, return the set of groups (by idx) satisfying the condition that all points in that group are ‚â§ Œîv of the first point
function idxs_of_groups(v, Œîv)
	idx_start = 1
	groups = Vector{Int}[]
	group = [idx_start]
	for i in 2:length(v)
		if v[i] - v[idx_start] ‚â§ Œîv
			push!(group, i)
		else
			push!(groups, group)
			idx_start = i
			group = [i]
		end
	end
	push!(groups, group)
	
	return groups
end

# ‚ïî‚ïê‚ï° ed0feb32-85a2-4d3b-bb96-47eb18e29dd3
# Averages the xs and associated ys in each grouping of x
function bin_data(x, y; Œî=0.1)
	idxs = idxs_of_groups(x, Œî)
	groups = [(x[idx], y[idx]) for idx in idxs]
	x_avg, y_avg = Float64[], Measurement{Float64}[]
	for group in groups
		push!(x_avg, mean(group[1]))
		push!(y_avg, weightedmean(group[2]))
	end
		
	return x_avg, y_avg
end

# ‚ïî‚ïê‚ï° 7370a1d9-4f8e-4788-adac-b8be2bcc9643
function plot_phot!(ax, t_window, t, f, f_err; t_offset=0.0, relative_flux=false)
	t_rel = t .- t_offset
	
	if relative_flux
		Œîf, Œîf_err = f .- mean(f), f_err
		
	else
		Œîf, Œîf_err = f, f_err
	end
	
	# Original data
	errorbars!(ax, t_rel, Œîf, Œîf_err, color=(:darkgrey, 0.25))
	scatter!(ax, t_rel, Œîf, color=(:darkgrey, 0.25))
	
	# Averaged data
	t_avg, f_avg = bin_data(t_rel, Œîf .¬± Œîf_err, Œî=t_window)
	f_unc_avg = uncertainty.(f_avg) |> mean
	errorbars!(ax, t_avg, f_avg, color=COLORS[2])
	scatter!(ax, t_avg, f_avg, color=COLORS[2], label="avg err: $(f_unc_avg)")
	
	axislegend(ax)
	
	return ax, t_avg, value.(f_avg), uncertainty.(f_avg)
end

