md"""
To visualize the spread of each parameter, we define the dimensionless metric:

```math
Œîx \equiv \frac{x - \overline{\text{BMA}}_Œº}{\overline{\text{BMA}}_œÉ}\quad,
```

where $x$ is a sample from the posterior distribution for the given parameter, ``\overline{\text{BMA}}_Œº`` is the BMA averaged across nights, and ``\overline{\text{BMA}}_œÉ`` is the maximum uncertainty, also averaged across nights. These values correspond to the `Combined` column in the table above. ``\Delta x`` is then a measure of the displacement of each sample in its posterior distribution from its corresponding average BMA value, scaled by the uncertainty in the average BMA.
"""

# ‚ïî‚ïê‚ï° 706f1fb6-2895-48f6-a315-842fbf35da18
function scale_samples(samples, param, BMA)
	m = @where(BMA, :Parameter .== param)[!, "Combined"][1]
	return (samples .- m.val) ./ m.err
end

